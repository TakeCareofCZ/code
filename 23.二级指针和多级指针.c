#include <stdio.h>
int main()
{
	int a = 10;
	int b = 20;

	//一级指针
	int* p = &a;

	//二级指针
	//不管是普通变量还是指针变量，将变量值赋给指针时都需要在变量名前加"&"
	int** pp = &p;

	//作用1：利用二级指针修改一级指针里面记录的内存地址
	//解引用符*加一级指针表示内存里的变量值
	//*加二级指针就变成了内存里的地址值，这里*pp也就等于p的地址值
	*pp = &b;

	printf("%p\n", &a);
	printf("%p\n", &b);
	printf("%p\n", p);

	//作用2：
	//双解引用符**，第一次将二级指针解引用为一级指针，第二次将一级指针解引用为指向地址中的变量值
	printf("%d\n", **pp);
	return 0;
}